    private void buttonEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonEntrarActionPerformed
        // TODO add your handling code here:
        login = textLogin.getText();
        senha = textSenha.getText();
        //HomePage home = new HomePage();
        
        if (("cliente@email.com".equals(login)) && ("123".equals(senha))){
            home.setVisible(true);
            setVisible(false);
        } else if(("fornecedor@email.com".equals(login)) && ("1234".equals(senha))){
            homefor.setVisible(true);
            setVisible(false);
        } else {
            JOptionPane.showMessageDialog(rootPane, "Usuário não encontrado. Verifique suas credenciais ou cadastre-se.", "Login Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_buttonEntrarActionPerformed

